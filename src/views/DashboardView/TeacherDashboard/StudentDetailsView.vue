            <div class="session-item">
              {{ formatTimestamp(session.createdAt) }}
            </div>
            <div class="session-item">{{ session.length }} Minutes</div>
          </div>
          <div class="summary-row" v-if="session.summary">
            <div class="session-summary-wrapper">
              <div class="session-summary">
                <div class="icon-and-tooltip">
                  <BlueAlert class="alert-icon" />
                  <div class="summary-tooltip">
                    This summary was generated by AI and may not be 100%
                    accurate.
                  </div>
