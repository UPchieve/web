              @update:modelValue="submitFilter"
            />
          </div>
        </div>
      </div>
      <div></div>
      <div class="sessions-container">
        <loader v-if="isLoading" />
        <div class="sessions-header">
          <div class="header-item">Subject</div>
          <div class="header-item"># of Messages</div>
          <div class="header-item">Session Date</div>
          <div class="header-item">Session Length</div>
        </div>

        <div
          v-for="session in sessions"
          :key="session.id"
          class="session-group"
          :class="hasSessionSummary ? 'has-summary' : ''"
        >
          <div
            class="session-row"
            :class="hasSessionSummary && !session.summary ? 'summary-row' : ''"
          >
