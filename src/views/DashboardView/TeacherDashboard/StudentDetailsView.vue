              </div>
              <div class="summary-feedback">
                <span>Was this summary helpful?</span>
                <GenerationFeedback
                  name="session-review-feedback"
                  :analyticsServiceThumbsUp="{
                    eventName: EVENTS.SESSION_SUMMARY_THUMBS_UP,
                    eventData: { sessionId: session.id },
                  }"
                  :analyticsServiceThumbsDown="{
                    eventName: EVENTS.SESSION_SUMMARY_THUMBS_DOWN,
                    eventData: { sessionId: session.id },
                  }"
                  :analyticsServiceFollowup="{
                    eventName: EVENTS.SESSION_SUMMARY_FEEDBACK,
                  }"
                  :traceId="session.summary.traceId"
                />
              </div>
            </div>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import Loader from '@/components/Loader.vue'
import FormDateInput from '@/components/FormInputs/FormDateInput.vue'
import NetworkService from '@/services/NetworkService'
import { mapState } from 'vuex'
import StudentAvatar from '@/assets/user_avatars/student-avatar.svg'
import { dayjs } from '@/utils/time-utils'
import FormSelect from '@/components/FormInputs/FormSelect.vue'
import BlueAlert from '@/assets/blue-alert.svg'
import GenerationFeedback from '@/components/GenerationFeedback.vue'
import { EVENTS } from '@/consts'
import { getCurrentSchoolYearStartDate } from '@/utils/time-utils'

export default {
  name: 'student-details',
  components: {
    Loader,
    StudentAvatar,
    FormDateInput,
    FormSelect,
    BlueAlert,
    GenerationFeedback,
  },

  inject: ['classData'],

  data() {
    return {
      studentId: '',
      sessions: [],
      studentFirstName: '',
      studentLastName: '',
      isLoading: true,
      filters: {
        topic: {
          name: '',
        },
        sessionActivityFrom:
          getCurrentSchoolYearStartDate().format('YYYY-MM-DD'),
        sessionActivityTo: dayjs().format('YYYY-MM-DD'),
      },
      subjectPlaceholder: '',
      hasSessionSummary: false,
      EVENTS,
    }
  },
  computed: {
    className() {
      return this.classData?.name ?? ''
    },
    ...mapState({
      topics: (state) => state.subjects.topics,
      subjects: (state) => state.subjects.subjects,
    }),
  },

  async created() {
    this.studentId = this.$route.params.studentId

    await this.$store.dispatch('subjects/awaitTopics')
    if (this.classData?.topicId) {
      const topic = this.topics.find((t) => t.id === this.classData.topicId)
      this.filters.topic.name = topic.name
      this.subjectPlaceholder = topic.displayName
    } else {
      this.filters.topic.name = 'other'
      this.subjectPlaceholder = 'All Subjects'
    }

    this.sessions = await this.getStudentSessionDetails()
  },

  methods: {
    formatTimestamp(timestamp) {
      const date = dayjs(timestamp)
      return date.format('MM/DD/YYYY')
    },

    async getStudentSessionDetails() {
      this.isLoading = true
      try {
        const {
          data: { sessionDetails },
        } = await NetworkService.getStudentSessionDetails(this.studentId)

        const filteredSessionDetails = sessionDetails.sort(
          (a, b) => new Date(b.createdAt) - new Date(a.createdAt)
        )

        if (filteredSessionDetails.length === 0) {
          return []
        }

        this.studentFirstName = filteredSessionDetails[0].firstName
        this.studentLastName = filteredSessionDetails[0].lastName
        const subjects = this.subjects
        const sessionsWithSubjects = filteredSessionDetails.map((session) => {
          const created = new Date(session.createdAt)
          const ended = new Date(session.endedAt)
          const sessionLength = (ended - created) / (1000 * 60)
          if (session.summary) this.hasSessionSummary = true
          return {
            ...session,
            sessionSubject: subjects[session.name].displayName,
            length: Math.round(sessionLength),
          }
        })
        return this.filterSessions(
          sessionsWithSubjects,
          this.subjects,
          this.filters
        )
      } catch {
        return []
      } finally {
        this.isLoading = false
      }
    },

    filterSessions(sessions, subjects, filters) {
      const start = new Date(filters.sessionActivityFrom + 'T00:00')
      const end = new Date(filters.sessionActivityTo + 'T23:59:59')
      const filteredSubjects = Object.values(subjects)
        .filter(
          (subject) =>
            filters.topic.name === 'other' ||
            subject.topicName === filters.topic.name
        )
        .map((subject) => subject.name)

      const filtered = sessions.filter((session) => {
        const createdAt = new Date(session.createdAt)
        return (
          createdAt >= start &&
          createdAt <= end &&
          filteredSubjects.includes(session.name)
        )
      })

      return filtered
    },

    async submitFilter() {
      this.sessions = await this.getStudentSessionDetails()
    },
  },
}
</script>

